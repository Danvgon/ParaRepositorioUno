public class clase01 {
	int numero;
	int cosa;
	String letra;

	public clase01(int numero, int cosa, String letra) {
		super();
		this.numero = numero;
		this.cosa = cosa;
		this.letra = letra;
	}

	public int getNumero() {
		return numero;
	}

	public void setNumero(int numero) {
		this.numero = numero;
	}

	public int getCosa() {
		return cosa;
	}

	public void setCosa(int cosa) {
		this.cosa = cosa;
	}

	public String getLetra() {
		return letra;
	}

	public void setLetra(String letra) {
		this.letra = letra;
	}

}
